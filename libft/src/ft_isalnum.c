#include "../include/libft.h"

int	ft_isalnum(unsigned char c)
{
	if (c >= '0' && c <= '9')
		return (8);
	else if (c >= 'a' && c <= 'z')
		return (8);
	else if (c >= 'A' && c <= 'Z')
		return (8);
	else
		return (0);
}
